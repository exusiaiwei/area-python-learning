def square_root(number):
    """
    Calculate and return the square root of the given number.

    Args:
        number (float): The number to calculate the square root for.

    Returns:
        float: The square root of the given number.

    Examples:
        >>> square_root(4)
        2.0
        >>> square_root(9)
        3.0
    """
    return number ** 0.5
